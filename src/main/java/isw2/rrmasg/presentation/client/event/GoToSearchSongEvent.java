package isw2.rrmasg.presentation.client.event;

import com.google.gwt.event.shared.GwtEvent;

public class GoToSearchSongEvent extends GwtEvent<GoToSearchSongEventHandler> {
	public static Type<GoToSearchSongEventHandler> TYPE = new Type<GoToSearchSongEventHandler>();

	protected void dispatch(GoToSearchSongEventHandler arg0) {
		arg0.onGoToSearchSong(this);
	}

	public Type<GoToSearchSongEventHandler> getAssociatedType() {
		return TYPE;
	}

}
